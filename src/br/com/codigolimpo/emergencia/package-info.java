/** 4 REGRAS DE UM PROJETO SIMPLES:
 * REGRA 1 - Efetuar todos os testes:
 *  Um sistema que é desenvolvido deve ser capaz de ser testado, se um sistema escrito no papel nem sempre vai ter tudo criado certo, é preciso que o mesmo passe em todos
 *  os testes possíveis, se o sistema não permite testar todas as suas funcionalidades o mesmo não deve ser implementado.
 * REGRA 2 - Sem Duplicações de código:
 *  Linhas de códigos muito parecidas ou métodos que fazem praticamente a mesma coisa podem ser refatorados para diminuir a complexibilidade do sistema, tornando o mesmo mais
 *  limpo e organizado, então buscar a díminuição de ambiguidades é uma boa maneira de melhorar seu código.
 * REGRA 3 - Expressar o propósito do programador:
 *  Nosso código deve se auto expressar, qualquer um que deseje ler o nosso código deve ser capaz de entendelo, assim nos podemos seguir o principios do "código limpo", para
 *  tentar garantir que nossos códigos sejam legiveis para todos.
 * REGRA 4 - Minimizar o número de classes e métodos:
 *  Tem sus importância mas não é uma regra que sempre vai ser colocada em prática, já que as outras tem uma maior importância na criação de uma sistema, mas não podemos de tentar
 *  deixa nossas classes e métodos com um tamanho reduzido.
 */